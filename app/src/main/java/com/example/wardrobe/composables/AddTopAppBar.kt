@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun AddTopAppBar(
    onNavigateBack: () -> Unit,
    isEditMode: Boolean,
    isOutfit: Boolean
) {
    var showDialog by remember { mutableStateOf(false) }
    if (showDialog) {
        AlertDialog(
            onDismissRequest = {
                showDialog = false
            },
            title = {
                Text(text = "Bist du sicher?")
            },
            text = {
                Text("Wenn du zurückgehst, werden deine Eingaben nicht gespeichert.")
            },
            confirmButton = {
                TextButton(
                    onClick = {
                        showDialog = false
                        onNavigateBack()
                    }
                ) {
                    Text("Ja")
                }
            },
            dismissButton = {
                TextButton(
                    onClick = {
                        showDialog = false
                    }
                ) {
                    Text("Nein")
                }
            }
        )
    }
    TopAppBar(
        title = {
            Text(
                text = (if (isEditMode) "Edit " else "Add ") + (if (isOutfit) "Outfit" else "Item"),
                fontWeight = FontWeight.Bold
            )
        },
        navigationIcon = {
            IconButton(onClick = { showDialog = true }) {
                Icon(Icons.Default.Close, contentDescription = "Close screen")
            }
        }
    )
}
